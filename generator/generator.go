package generator

import (
	"fmt"
	"io"

	"github.com/devimteam/microgen/generator/template"
	"github.com/vetcher/jennifer/jen"
)

type Renderer interface {
	Render(io.Writer) error
}

type Generator interface {
	Generate() error
}

type Strategy interface {
	Write(Renderer) error
}

type generationUnit struct {
	template Template
	info     *template.GenerationInfo

	writeStrategy Strategy
	outPath       string
}

func NewGenUnit(tmpl Template, info *template.GenerationInfo, write Strategy, outPath string) generationUnit {
	return generationUnit{
		template:      tmpl,
		info:          info,
		writeStrategy: write,
		outPath:       outPath,
	}
}

func (g *generationUnit) Generate() error {
	statement := g.template.Render(g.info)
	if g.info.Force {
		file := jen.NewFile("sss")
		file.PackageComment(`This file was automatically generated by "microgen" utility.`)
		file.PackageComment(`Please, do not edit.`)
		file.Add(statement)
		err := g.writeStrategy.Write(file)
		if err != nil {
			return fmt.Errorf("write error: %v", err)
		}
	} else {
		err := g.writeStrategy.Write(statement)
		if err != nil {
			return fmt.Errorf("write error: %v", err)
		}
	}
	return nil
}

